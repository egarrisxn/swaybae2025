import { client } from "./client";
import { postSitemapQuery, tagSitemapQuery } from "./queries";

export async function sitemapData() {
  try {
    const posts = await client.fetch(postSitemapQuery);
    const tags = await client.fetch(tagSitemapQuery);

    return { posts, tags };
  } catch (error) {
    console.error("Error fetching sitemap data:", error);
    return { posts: [], tags: [] };
  }
}
